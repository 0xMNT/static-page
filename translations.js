// Current language (default to Romanian)
let currentLang = localStorage.getItem('language') || 'ro';

// Function to change language
function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    updateContent();
    updateLanguageUI();
}

// Function to update all content on the page
function updateContent() {
    const t = translations[currentLang];

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            element.textContent = t[key];
        }
    });
}

// Function to update language UI (active states)
function updateLanguageUI() {
    // Update desktop language toggle
    const langToggle = document.querySelector('.lang-toggle');
    if (langToggle) {
        langToggle.textContent = currentLang.toUpperCase();
    }

    // Update mobile menu language links
    document.querySelectorAll('.lang-link').forEach(link => {
        link.classList.remove('active');
        const linkLang = link.getAttribute('data-lang');
        if (linkLang === currentLang) {
            link.classList.add('active');
        }
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    updateContent();
    updateLanguageUI();
});
